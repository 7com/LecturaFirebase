    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean valid = true;
        try {
            float f = Float.parseFloat(jTextField1.getText());
        } catch (NumberFormatException ex) {
            valid=false;
            JOptionPane.showMessageDialog(null,"Temperatura no es un float v치lido.","Error",JOptionPane.ERROR_MESSAGE);
        }
        try {
            float f = Float.parseFloat(jTextField2.getText());
        } catch (NumberFormatException ex) {
            valid=false;
            JOptionPane.showMessageDialog(null,"Voltaje no es un float v치lido.","Error",JOptionPane.ERROR_MESSAGE);
        }
        if (valid){
            FirebaseDatabase database = FirebaseDatabase.getInstance();
            DatabaseReference ref = database.getReference("*Config");
            DatabaseReference dr = ref.child("maxTemp");
            dr.setValue(jTextField1.getText());
            dr = ref.child("maxVolt");
            dr.setValue(jTextField2.getText());
            p.setButton1(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

